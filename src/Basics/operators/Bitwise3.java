package Basics.operators;

public class Bitwise3 {
    public static void main(String args[]){
        int a=880;
        int b=206;
        System.out.println(a>b&&a<b);
        System.out.println(a<b&&a<b);
        System.out.println(a<b&&a>b);
        System.out.println(a>b&&a>b);
        System.out.println(a>b||a<b);
        System.out.println(a<b||a<b);
        System.out.println(a<b||a>b);
        System.out.println(a>b||a>b);





    }
}

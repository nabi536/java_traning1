package Basics.operators;

public class Bitwise7 {
    public static void main(String args[]){
        int a=6580;
        int b=2320;
        System.out.println(a>b&&a<b);
        System.out.println(a<b&&a<b);
        System.out.println(a<b&&a>b);
        System.out.println(a>b&&a>b);
        System.out.println(a>b||a<b);
        System.out.println(a<b||a<b);
        System.out.println(a<b||a>b);
        System.out.println(a>b||a>b);





    }
}

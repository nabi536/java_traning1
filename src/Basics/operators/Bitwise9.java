package Basics.operators;

public class Bitwise9 {
    public static void main(String args[]){
        int a=808;
        int b=2450;
        System.out.println(a>b&&a<b);
        System.out.println(a<b&&a<b);
        System.out.println(a<b&&a>b);
        System.out.println(a>b&&a>b);
        System.out.println(a>b||a<b);
        System.out.println(a<b||a<b);
        System.out.println(a<b||a>b);
        System.out.println(a>b||a>b);





    }
}

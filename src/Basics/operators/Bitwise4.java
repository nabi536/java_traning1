package Basics.operators;

public class Bitwise4 {
    public static void main(String args[]){
        int a=450;
        int b=780;
        System.out.println(a>b&&a<b);
        System.out.println(a<b&&a<b);
        System.out.println(a<b&&a>b);
        System.out.println(a>b&&a>b);
        System.out.println(a>b||a<b);
        System.out.println(a<b||a<b);
        System.out.println(a<b||a>b);
        System.out.println(a>b||a>b);





    }
}

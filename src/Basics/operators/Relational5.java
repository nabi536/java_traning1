package Basics.operators;

public class Relational5 {
    public static void main(String args[]){
        int a = 329;
        int b=45;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}

package Basics.operators;

public class Relational10 {
    public static void main(String args[]){
        int a = 5;
        int b=95;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(b>=a);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}

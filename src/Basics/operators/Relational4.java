package Basics.operators;

public class Relational4 {
    public static void main(String args[]){
        int a = 543;
        int b=425;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}

package Basics.operators;

public class Unary3 {
    public static void main(String args[]){
        int a = 94;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);


    }
}

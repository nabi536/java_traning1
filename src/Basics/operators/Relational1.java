package Basics.operators;

public class Relational1 {
    public static void main(String args[]){
        int a = 25;
        int b=45;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}

package Basics.operators;

public class Bitwise2 {
    public static void main(String args[]){
        int a=79;
        int b=30;
        System.out.println(a>b&&a<b);
        System.out.println(a<b&&a<b);
        System.out.println(a<b&&a>b);
        System.out.println(a>b&&a>b);
        System.out.println(a>b||a<b);
        System.out.println(a<b||a<b);
        System.out.println(a<b||a>b);
        System.out.println(a>b||a>b);





    }
}

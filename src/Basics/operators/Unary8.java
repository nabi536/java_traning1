package Basics.operators;

public class Unary8 {
    public static void main(String args[]){
        int a = 79;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);


    }
}

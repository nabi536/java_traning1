package Basics.operators;

public class Relational7 {
    public static void main(String args[]){
        int a = 325;
        int b=9009;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}

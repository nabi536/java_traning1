package Basics;

public class UrinaryOperator {
    public static void main(String[] args){
        int a = 25;
        System.out.println(++a);//a=26
        System.out.println(a++);//a=26 +1
        System.out.println(a);//a=27
        System.out.println(--a);//a=26
        System.out.println(a--);//a=26 -1
        System.out.println(a); //a=25
    }
}

public class NumberCheck {
    public static void main(String[] args) {
        int a = 10;
        int b = 12;
        if(a==b){
            System.out.println("True");
        }if(a<=b){
            System.out.println("True");
        }else if(a>=b){
            System.out.println("False");
        }
    }
}
